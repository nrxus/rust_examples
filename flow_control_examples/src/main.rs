fn if_else_example() {
    let n = 5;

    if n < 0 {
        print!("{} is negative", n);
    } else if n > 0 {
        print!("{} is positive", n);
    } else {
        print!("{} is zero", n);
    }

    let big_n = if n < 10 && n > -10 {
        println!(", and is a small number, increase ten-fold");
        10 * n
    } else {
        println!(", and is a big number, reduce by two");
        n / 2
    };

    println!("{} -> {}", n, big_n);
}

fn main() {
    if_else_example();
}
